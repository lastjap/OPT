package com.opt.dto;


public class CouponDto {
	private int opt_no_seq;
	private int coupon_no_seq;
	private int coupon_no;
	private String coupon_name;
	private String coupon_content;
	private String coupon_date;
	private String coupon_sale;
	public int getOpt_no_seq() {
		return opt_no_seq; 
	}
	public void setOpt_no_seq(int opt_no_seq) {
		this.opt_no_seq = opt_no_seq;
	}
	public int getCoupon_no_seq() {
		return coupon_no_seq;
	}
	public void setCoupon_no_seq(int coupon_no_seq) {
		this.coupon_no_seq = coupon_no_seq;
	}
	public int getCoupon_no() {
		return coupon_no;
	}
	public void setCoupon_no(int coupon_no) {
		this.coupon_no = coupon_no;
	}
	public String getCoupon_name() {
		return coupon_name;
	}
	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}
	public String getCoupon_content() {
		return coupon_content;
	}
	public void setCoupon_content(String coupon_content) {
		this.coupon_content = coupon_content;
	}
	public String getCoupon_date() {
		return coupon_date;
	}
	public void setCoupon_date(String coupon_date) {
		this.coupon_date = coupon_date;
	}
	public String getCoupon_sale() {
		return coupon_sale;
	}
	public void setCoupon_sale(String coupon_sale) {
		this.coupon_sale = coupon_sale;
	}
	
}
